function toggleColor1() {

   var myButtonClasses = document.getElementById("btm-teh1").classList;
  
   if (myButtonClasses.contains("teh-item-100")) {
      myButtonClasses.remove("teh-item-100");
  
   } else {
      myButtonClasses.add("teh-item-100");
   }
}
